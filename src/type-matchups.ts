export const getOffensiveMatchups = (index: TypesIndex) => {
  const normal = [];
  const noEffect = [];
  const superEffective = [];
  const notEffective = [];

  for (let i = 0; i < TYPES_MATCHUP_MATRIX[index].length; i++) {
    switch (TYPES_MATCHUP_MATRIX[index][i]) {
      case 0:
        noEffect.push(i);
        break;
      case 1:
        normal.push(i);
        break;
      case 0.5:
        notEffective.push(i);
        break;
      case 2:
        superEffective.push(i);
        break;
    }
  }
  return {
    normal,
    noEffect,
    notEffective,
    superEffective,
  };
};

